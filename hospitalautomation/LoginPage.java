    private void patientLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_patientLoginActionPerformed
        // TODO add your handling code here:
        if (txt_idPatient.getText().length() == 0 || txt_passPatient.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields", "ERROR", JOptionPane.ERROR_MESSAGE);
	} else {
	boolean key = true;

	try {
            // Connect to the database
	    Connection con = conn.connDb();
	    Statement st = con.createStatement();
	    ResultSet rs = st.executeQuery("SELECT * FROM user");
            
            // Loop through the result set to check credentials
	    while (rs.next()) {
	    if (txt_idPatient.getText().equals(rs.getString("tcno"))&& txt_passPatient.getText().equalsIgnoreCase(rs.getString("password"))) {
                
                // If user type is "hasta" (patient), initialize PatientPage
	    if (rs.getString("type").equals("hasta")) {
	        Patient hasta = new Patient();
		hasta.setId(rs.getInt("iduser"));
		hasta.setPassword("password");
		hasta.setTcno(rs.getString("tcno"));
		hasta.setName(rs.getString("name"));
		hasta.setType(rs.getString("type"));
		PatientPage hGUI = new PatientPage(hasta);
		hGUI.setVisible(true);
		dispose();  // Close the current login window
		key = false;  // Login successful, no need to show error
	    }
           }
	  }
	 } catch (Exception e1) {
	          e1.printStackTrace();  // Handle any exceptions
	}
	if (key)
            JOptionPane.showMessageDialog(null, "No such patient was found, please register.", "ERROR", JOptionPane.ERROR_MESSAGE);

	}
    }//GEN-LAST:event_patientLoginActionPerformed

