    private void doctorLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doctorLoginActionPerformed
        // TODO add your handling code here:
        String id = txt_idDoctor.getText();
        String password = new String(txt_passDoctor.getPassword());
        if (id.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
            try {
                 Connection con = conn.connDb();
		 Statement st = con.createStatement();
		ResultSet rs = st.executeQuery("SELECT * FROM user");
		while (rs.next()) {
		if (id.equals(rs.getString("tcno"))
				&& password.equals(rs.getString("password"))) {
                    
                    // If user type is "bashekim" (chief doctor), initialize BashekimPage
			if (rs.getString("type").equals("bashekim")) {
			Bashekim bhekim = new Bashekim();
			bhekim.setId(rs.getInt("iduser"));
			bhekim.setPassword(rs.getString("password"));
			bhekim.setTcno(rs.getString("tcno"));
			bhekim.setName(rs.getString("name"));
			bhekim.setType(rs.getString("type"));
			BashekimPage bGUI = new BashekimPage(bhekim);
			bGUI.setVisible(true);
			dispose();
			}
                        
                        // If user type is "doktor" (doctor), initialize DoctorPage
                        if (rs.getString("type").equals("doktor")) {
                        System.out.println("Doktor tipi doğru eşleşti.");
			Doctor doctor = new Doctor();
			doctor.setId(rs.getInt("iduser"));
			doctor.setPassword(rs.getString("password"));
			doctor.setTcno(rs.getString("tcno"));
			doctor.setName(rs.getString("name"));
			doctor.setType(rs.getString("type"));
                        System.out.println("Doctor nesnesi oluşturuldu: " + doctor.getName());        
			DoctorPage dGUI = new DoctorPage(doctor);
			dGUI.setVisible(true);
			dispose();
		}
	}
	}
	} catch (Exception e1) {
	e1.printStackTrace();
}

				
    }//GEN-LAST:event_doctorLoginActionPerformed

