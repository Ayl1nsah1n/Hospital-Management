    /**
     * Updates the working hour table model for the given doctor.
     *
     * @param doctor The doctor whose working hour data needs to be updated.
     * @throws SQLException If a database error occurs.
     */
    public void updateWhourModel(Doctor doctor) throws SQLException {
		DefaultTableModel clearModel = (DefaultTableModel) whour_table.getModel();
		clearModel.setRowCount(0);
		for (int i = 0; i < doctor.getWhourList(doctor.getId()).size(); i++) {
			whourData[0] = doctor.getWhourList(doctor.getId()).get(i).getId();
			whourData[1] = doctor.getWhourList(doctor.getId()).get(i).getWdate();
			whourModel.addRow(whourData);

		}
	}
    
    
    
    
    
    
    
    

