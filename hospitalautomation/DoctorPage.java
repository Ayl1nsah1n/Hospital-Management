    public DoctorPage(Doctor doctor) throws SQLException {
        initComponents();
        this.doctor = doctor;
        
         // Initialize the working hour table model
        whourModel = new DefaultTableModel();
        Object[] colWhour = new Object[2];
		colWhour[0] = "ID";  // Column for working hour ID
		colWhour[1] = "DATE";  // Column for working hour date
		whourModel.setColumnIdentifiers(colWhour);
		whourData = new Object[2];
		try {
                    // Populate the working hour table with data
			for (int i = 0; i < doctor.getWhourList(doctor.getId()).size(); i++) {
				whourData[0] = doctor.getWhourList(doctor.getId()).get(i).getId();
				whourData[1] = doctor.getWhourList(doctor.getId()).get(i).getWdate();
				whourModel.addRow(whourData);

			}
                        whour_table.setModel(whourModel);
		} catch (SQLException e1) {
			// TODO Auto-generated catch block
			e1.printStackTrace();
		}

        }
